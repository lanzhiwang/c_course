#include <stdio.h>

int main(void)
{
	/**
	限定输出的"最小"宽度
	 */
	printf("|%5d|\n", 123);
	printf("|%5d|\n", 12345);
	printf("|%5d|\n", 123456);
	/**
	|  123|
	|12345|
	|123456|
	 */

	/**
	默认是右对齐, 使用 - 指定成左对齐
	 */
	printf("|%5d|\n", 123);
	printf("|%-5d|\n", 123);
	printf("|%-5d|\n", 12345);
	printf("|%-5d|\n", 123456);
	/**
	|  123|
	|123  |
	|12345|
	|123456|
	 */

	/**
	显示正负号, 使用 +
	 */
	printf("|%+5d|\n", 123);
	printf("|%+5d|\n", -123);
	/**
	| +123|
	| -123|
	 */

	////////////////////////////////////////////////////////

	printf("|%f|\n", 12.34);
	printf("|%12f|\n", 12.34);
	/**
	|12.340000|
	|   12.340000|
	 */

	/**
	限制浮点数小数点后的位数
	 */
	printf("|%f|\n", 12.1234567);
	printf("|%.3f|\n", 12.1234567);
	printf("|%.3f|\n", 12.1);

	printf("|%+12.3f|\n", 12.1234567);
	printf("|%+-12.3f|\n", 12.1234567);
	/**
	|12.123457|
	|12.123|
	|12.100|
	|     +12.123|
	|+12.123     |
	 */

	/**
	使用 * 表示"最小"宽度和小数点后的位数
	 */
	printf("|%+-*.*f|\n", 12, 3, 12.1234567);
	/**
	|+12.123     |
	 */

	/**
	输出部分字符串
	 */
	printf("|%s|\n", "helloworld");
	printf("|%.5s|\n", "helloworld");
	/**
	|helloworld|
	|hello|
	 */

	return 0;
}

